import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const url = process.env.OPENAPI_URL || "http://localhost:8000/openapi.json";
const out = resolve(process.cwd(), "lib/api/generated/schema.ts");

const response = await fetch(url);
if (!response.ok) {
  throw new Error(`Failed to fetch OpenAPI schema from ${url}: ${response.status} ${response.statusText}`);
}

const schema = await response.json();
const body = `// Generated by scripts/generate-openapi-types.mjs. Do not edit by hand.\n` +
  `export const openApiSchema = ${JSON.stringify(schema, null, 2)} as const;\n\n` +
  `export type OpenApiSchema = typeof openApiSchema;\n` +
  `export type ApiPath = keyof OpenApiSchema["paths"];\n`;

mkdirSync(dirname(out), { recursive: true });
writeFileSync(out, body);
console.log(`Wrote ${out}`);
